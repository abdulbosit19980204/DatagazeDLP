Xodimdan skrinshotlar / ma'lumotlar kelmayapti

1. Muammo dastlab agendami yoki serverdami shuni tekshirib olamiz

2. Dastlab ishni agentni tekshirishdan boshlagan yaxshi
3. Skrinshot olinganmi yoqmi shuni tekshiramiz 
    Buning uchun DbParser ni ochamiz va skrin bolimini korib chiqamiz
4. Agar skrinshot olingan bolsa keyingi qadam jonatilganmi yoqmi shuni tekshirish 
    Buning uchun Log bolimini korib chiqamiz
5. Skrinshotlar serverga jonatilgan bolsa lekin serverda qabul qilinmagan bolsa 
    sereverni tekshirishni boshlaymiz
6. Buning uchun Serverdagi service larni tekshirish kerak 
7. Bazan DbParser dagi Screen bolimini ham Error bolimini ham korib chiqish kerak boladi
